export async function resetDatabase() {

    let response = await fetch(URL + "lastupdate" + ".json");
    let lastUpdate = await response.json();

    const fifteenMinutesInMs = 15 * 60 * 1000; // 15 minutes in milliseconds
    if (Date.now() - lastUpdate >= fifteenMinutesInMs) {

        await fetch(URL + '/tasks' + ".json", {
            method: "PUT",
            headers: {
                "Content-Type": "application/json",
            },
            body: JSON.stringify(tasks),
        });

        await fetch(URL + '/contacts' + ".json", {
            method: "PUT",
            headers: {
                "Content-Type": "application/json",
            },
            body: JSON.stringify(contacts),
        });

        await fetch(URL + '/lastupdate' + ".json", {
            method: "PUT",
            headers: {
                "Content-Type": "application/json",
            },
            body: JSON.stringify(Date.now()),
        });

    }
}

